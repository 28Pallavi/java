package logical;

public class Logical {

	public static void main(String[] args) {
		  boolean a = true, b = false;
		  
		  //Logical AND
		  System.out.println("a1 && b = " + (a && b));
		  
		  //Logical OR
		  System.out.println("a || b = " + (a || b) );
		  
		  //Logical Not
		  System.out.println("!(a && b) = " + !(a && b));

	}

}
