package CollectionEg;

public class Employee {

	int emp_id;
	String emp_name;
	long emp_ph;
	int dept_id;
	
	
	public Employee(int emp_id, String emp_name, long emp_ph , int dept_id) {
		super();
		this.emp_id = emp_id;
		this.emp_name = emp_name;
		this.dept_id = dept_id;
		this.emp_ph = emp_ph;
	}
	
}
